package org.ex.fh.data_model;

import java.sql.Date;

/**
 *
 * @author dgmin
 */
public class BillDetail {
    
    private int     id;
    private Date    date;

    public BillDetail(int id, Date date) {
        this.id = id;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}